class Pokemon{

    constructor(name, health, hitPoints, energyType, move, weakness, resistance){

        this.name = name;
        this.health = health;
        this.hitPoints = hitPoints;
        this.energyType = energyType;
        this.move = move;
        this.weakness = weakness;
        this.resistance = resistance;


        function attack(){
            this.move
        }
    }
}